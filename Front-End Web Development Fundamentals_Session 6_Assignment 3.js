                // Here is the example of sum of numbers using javascript

                // Sum using for loop
                
                var sum = 0;
				for (var i = 1; i <= 1000; i++) {
				   sum = sum + i;
				}
				console.log("Sum using for loop = " + sum);

                //Sum using while loop

                var sum = 0;
				var number = 1;
				while (number <= 1000) {  
				  sum += number;        
				  number++;           
				}
				console.log("Sum using while loop = " + sum); 